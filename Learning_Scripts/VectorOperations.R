# Create two numeric vectors, x and y, each with 5 elements.
x <- c(1, 2, 3, 4, 5)
y <- c(6, 7, 8, 9, 10)

# Compute their sum, difference, product, and quotient element-wise.
sum_xy <- x + y
diff_xy <- x - y
prod_xy <- x * y
qua_xy <- x / y

# Find the mean and standard deviation of each.
avg_x <- mean(x)
avg_y <- mean(y)
sd_x <- sd(x)
sd_y <- sd(y)

# Print results
sum_xy; diff_xy; prod_xy; qua_xy
avg_x; sd_x
